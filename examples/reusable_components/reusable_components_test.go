func TestASTRender(t *testing.T) {
	c := qt.New(t)
	table := ast.NewCreateTable("users").
		AddColumn(ast.NewColumn("id", "SERIAL").SetPrimary()).
		AddColumn(ast.NewColumn("email", "TEXT").SetNotNull().SetUnique())

	sql, err := renderer.RenderSQL("postgres", table)

	c.Assert(err, qt.IsNil)
	c.Assert(sql, qt.Contains, `CREATE TABLE "users"`)
	c.Assert(sql, qt.Contains, `"email" TEXT UNIQUE NOT NULL`)
}

func TestGoAnnotationsToSQL(t *testing.T) {
	c := qt.New(t)
	fsys := fstest.MapFS{
		"models/user.go": {
			Data: []byte(`package models

//migrator:schema:table name="users"
type User struct {
	//migrator:schema:field name="id" type="INTEGER" primary="true"
	ID int

	//migrator:schema:field name="email" type="TEXT" not_null="true" unique="true"
	Email string
}
`),
		},
	}

	db, err := goschema.ParseFS(fsys, "models")
	c.Assert(err, qt.IsNil)

	statements, err := renderer.GetOrderedCreateStatements(db, "sqlite")

	c.Assert(err, qt.IsNil)
	c.Assert(statements, qt.HasLen, 1)
	c.Assert(statements[0], qt.Contains, `CREATE TABLE "users"`)
}

func TestAtlasHCLToSQL(t *testing.T) {
	c := qt.New(t)
	db, err := atlascompat.ParseAtlasHCL([]byte(`
schema "public" {}

table "users" {
  schema = schema.public
  column "id" {
    type = int
  }
  primary_key {
    columns = [column.id]
  }
}
`), "schema.hcl")
	c.Assert(err, qt.IsNil)

	list := atlascompat.SchemaToAST(*db, "postgres")
	sql, err := renderer.RenderSQL("postgres", list.Statements...)

	c.Assert(err, qt.IsNil)
	c.Assert(sql, qt.Contains, `CREATE TABLE "public"."users"`)
}

func TestDiffAndPlan(t *testing.T) {
	c := qt.New(t)
	desired := &goschema.Database{
		Tables: []goschema.Table{{StructName: "User", Name: "users"}},
		Fields: []goschema.Field{{
			StructName: "User",
			FieldName:  "ID",
			Name:       "id",
			Type:       "INTEGER",
			Primary:    true,
		}},
	}
	live := &dbschematypes.DBSchema{}

	diff := schemadiff.CompareWithDialect(desired, live, "sqlite")
	statements, err := planner.GenerateSchemaDiffSQLStatements(diff, desired, "sqlite")

	c.Assert(err, qt.IsNil)
	c.Assert(statements, qt.HasLen, 1)
	c.Assert(statements[0], qt.Contains, `CREATE TABLE "users"`)
}

func TestMigrationIntegrityAndLint(t *testing.T) {
	c := qt.New(t)
	fsys := fstest.MapFS{
		"0000000001_create_users.up.sql": {
			Data: []byte("CREATE TABLE users (id INTEGER PRIMARY KEY);\n"),
		},
		"0000000001_create_users.down.sql": {
			Data: []byte("DROP TABLE users;\n"),
		},
	}

	sum, err := atlascompat.ComputeSum(fsys, migrator.MigrationDirFormatPtah)
	c.Assert(err, qt.IsNil)
	c.Assert(sum.Entries, qt.HasLen, 2)

	findings, err := lint.LintFS(fsys, lint.Options{Dialect: "sqlite"})
	c.Assert(err, qt.IsNil)
	c.Assert(findings, qt.HasLen, 0)
}

func TestCapabilitiesRender(t *testing.T) {
	c := qt.New(t)
	caps := capability.ForServerVersion("postgres", "17.0")
	table := ast.NewCreateTable("accounts").
		AddColumn(ast.NewColumn("id", "INTEGER").SetIdentity("BY_DEFAULT", "1", "1").SetPrimary())

	sql, err := renderer.RenderSQLWithCapabilities("postgres", caps, table)

	c.Assert(err, qt.IsNil)
	c.Assert(sql, qt.Contains, "GENERATED BY DEFAULT AS IDENTITY")
}
