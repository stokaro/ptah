func TestParse_IssueExampleMatchesGoAnnotations(t *testing.T) {
	c := qt.New(t)

	yamlDB, err := yamlschema.Parse([]byte(`
tables:
  users:
    columns:
      id: { type: SERIAL, primary: true }
      email: { type: VARCHAR(255), not_null: true, unique: true }
    indexes:
      idx_users_email: { fields: [email] }
`))
	c.Assert(err, qt.IsNil)

	goDB, err := goschema.ParseSource("schema.go", `
package test

//migrator:schema:table name="users"
type User struct {
	//migrator:schema:field name="id" type="SERIAL" primary="true"
	ID int64

	//migrator:schema:field name="email" type="VARCHAR(255)" not_null="true" unique="true"
	Email string

	//migrator:schema:index name="idx_users_email" fields="email"
	_ int
}
`)
	c.Assert(err, qt.IsNil)

	yamlSQL := strings.Join(renderer.GetOrderedCreateStatements(yamlDB, "postgres"), "\n")
	goSQL := strings.Join(renderer.GetOrderedCreateStatements(&goDB, "postgres"), "\n")
	c.Assert(yamlSQL, qt.Equals, goSQL)
}

func TestParse_IdentityColumn(t *testing.T) {
	c := qt.New(t)

	db, err := yamlschema.Parse([]byte(`
tables:
  users:
    columns:
      id:
        type: int
        not_null: true
        identity_generation: BY_DEFAULT
        identity_start: 10
        identity_increment: 5
        identity_options: START WITH 10 INCREMENT BY 5 CACHE 3
`))
	c.Assert(err, qt.IsNil)
	c.Assert(db.Fields, qt.HasLen, 1)
	c.Assert(db.Fields[0].AutoInc, qt.IsTrue)
	c.Assert(db.Fields[0].IdentityGeneration, qt.Equals, "BY_DEFAULT")
	c.Assert(db.Fields[0].IdentityStart, qt.Equals, "10")
	c.Assert(db.Fields[0].IdentityIncrement, qt.Equals, "5")
	c.Assert(db.Fields[0].IdentityOptions, qt.Equals, "START WITH 10 INCREMENT BY 5 CACHE 3")

	sql := legacyRenderedSQL(strings.Join(renderer.GetOrderedCreateStatements(db, "postgres"), "\n"))
	c.Assert(sql, qt.Contains, "GENERATED BY DEFAULT AS IDENTITY (START WITH 10 INCREMENT BY 5 CACHE 3)")
}

