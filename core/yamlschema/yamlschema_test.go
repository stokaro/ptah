func TestParse_CoversCurrentSchemaIR(t *testing.T) {
	c := qt.New(t)

	db, err := yamlschema.Parse([]byte(`
enums:
  account_status: [active, suspended]
extensions:
  pg_trgm:
    if_not_exists: true
functions:
  current_tenant:
    params: ""
    returns: TEXT
    language: SQL
    security: DEFINER
    volatility: STABLE
    body: SELECT current_setting('app.current_tenant_id')
roles:
  app_user:
    login: true
    inherit: false
grants:
  app_user_schema:
    role: app_user
    privilege: USAGE
    on_schema: public
  app_user_users:
    role: app_user
    privileges: [SELECT, INSERT]
    on_table: users
tables:
  tenants:
    columns:
      id: { type: SERIAL, primary: true }
  users:
    rls_enabled: true
    columns:
      id: { type: SERIAL, primary: true }
      tenant_id:
        type: INTEGER
        not_null: true
        foreign: tenants(id)
        foreign_key_name: fk_users_tenant
        on_delete: CASCADE
      status:
        type: account_status
        not_null: true
        default: active
      email:
        type: VARCHAR(255)
        not_null: true
        unique: true
        platform:
          mysql:
            type: VARCHAR(191)
      email_lc:
        type: TEXT
        generated: lower(email)
        stored: true
    indexes:
      idx_users_email:
        fields: [email]
        unique: true
        where: deleted_at IS NULL
    constraints:
      chk_users_email:
        type: CHECK
        check: "position('@' in email) > 1"
rls_policies:
  users_tenant_isolation:
    table: users
    for: ALL
    to: app_user
    using: tenant_id = current_tenant()::INTEGER
`))
	c.Assert(err, qt.IsNil)

	c.Assert(db.Tables, qt.HasLen, 2)
	c.Assert(db.Tables[0].Name, qt.Equals, "tenants")
	c.Assert(db.Tables[1].Name, qt.Equals, "users")
	c.Assert(db.Fields, qt.HasLen, 6)
	c.Assert(db.Fields[5].GeneratedExpression, qt.Equals, "lower(email)")
	c.Assert(db.Fields[5].GeneratedKind, qt.Equals, "STORED")
	c.Assert(db.Enums, qt.DeepEquals, []goschema.Enum{{Name: "account_status", Values: []string{"active", "suspended"}}})
	c.Assert(db.Extensions, qt.HasLen, 1)
	c.Assert(db.Functions, qt.HasLen, 1)
	c.Assert(db.Functions[0].Returns, qt.Equals, "text")
	c.Assert(db.Functions[0].Language, qt.Equals, "sql")
	c.Assert(db.Roles, qt.HasLen, 1)
	c.Assert(db.Roles[0].Inherit, qt.IsFalse)
	c.Assert(db.Grants, qt.HasLen, 2)
	c.Assert(db.Grants[0].Privileges, qt.DeepEquals, []string{"USAGE"})
	c.Assert(db.Grants[1].Privileges, qt.DeepEquals, []string{"SELECT", "INSERT"})
	c.Assert(db.Grants[1].OnTable, qt.Equals, "users")
	c.Assert(db.RLSEnabledTables, qt.HasLen, 1)
	c.Assert(db.RLSPolicies, qt.HasLen, 1)
	c.Assert(db.Constraints, qt.HasLen, 1)
	c.Assert(db.Indexes[0].Condition, qt.Equals, "deleted_at IS NULL")
	c.Assert(db.Dependencies["users"], qt.DeepEquals, []string{"tenants"})

	sql := legacyRenderedSQL(strings.Join(renderer.GetOrderedCreateStatements(db, "postgres"), "\n"))
	c.Assert(sql, qt.Contains, "email_lc TEXT GENERATED ALWAYS AS (lower(email)) STORED")
	c.Assert(sql, qt.Contains, "WHERE deleted_at IS NULL")
	c.Assert(sql, qt.Contains, `CONSTRAINT chk_users_email CHECK (position('@' in email) > 1)`)
	c.Assert(sql, qt.Contains, `ALTER TABLE users ENABLE ROW LEVEL SECURITY;`)
	c.Assert(sql, qt.Contains, `CREATE POLICY users_tenant_isolation ON users`)
	c.Assert(sql, qt.Contains, `GRANT USAGE ON SCHEMA public TO app_user;`)
	c.Assert(sql, qt.Contains, `GRANT SELECT, INSERT ON TABLE users TO app_user;`)
}

