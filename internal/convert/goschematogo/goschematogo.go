// Package goschematogo renders goschema databases as annotated Go source.
package goschematogo

import (
	"bytes"
	"fmt"
	"go/format"
	"go/token"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"
	"unicode"

	"github.com/stokaro/ptah/core/goschema"
)

const generatedHeader = "// Code generated by ptah introspect; DO NOT EDIT.\n\n"

// Options controls annotated Go source rendering.
type Options struct {
	PackageName     string
	PerTable        bool
	SingleFile      bool
	LowercaseFields bool
	AddJSONTags     bool
	AddDBTags       bool
}

// File is one generated Go file.
type File struct {
	Name string
	Data []byte
}

type attr struct {
	name  string
	value string
	set   bool
}

// Render returns annotated Go source files for db.
func Render(db *goschema.Database, opts Options) ([]File, error) {
	if db == nil {
		return nil, fmt.Errorf("database schema is nil")
	}
	opts = normalizeOptions(opts)
	if err := validatePackageName(opts.PackageName); err != nil {
		return nil, err
	}

	ctx := newRenderContext(db, opts)
	if opts.SingleFile {
		file, err := ctx.renderSingleFile()
		if err != nil {
			return nil, err
		}
		return []File{file}, nil
	}
	return ctx.renderPerTableFiles()
}

// WriteDir writes generated files to outDir.
func WriteDir(outDir string, files []File) error {
	if strings.TrimSpace(outDir) == "" {
		return fmt.Errorf("output directory is required")
	}
	if err := os.MkdirAll(outDir, 0o755); err != nil {
		return fmt.Errorf("create output directory: %w", err)
	}
	for _, file := range files {
		if strings.TrimSpace(file.Name) == "" {
			return fmt.Errorf("generated file has empty name")
		}
		if !isSafeFileName(file.Name) {
			return fmt.Errorf("unsafe generated file name %q", file.Name)
		}
		path := filepath.Join(outDir, file.Name)
		if err := os.WriteFile(path, file.Data, 0o600); err != nil {
			return fmt.Errorf("write %s: %w", path, err)
		}
	}
	return nil
}

func normalizeOptions(opts Options) Options {
	if opts.PackageName == "" {
		opts.PackageName = "models"
	}
	if !opts.SingleFile {
		opts.PerTable = true
	}
	return opts
}

func validatePackageName(name string) error {
	if !token.IsIdentifier(name) || token.Lookup(name).IsKeyword() {
		return fmt.Errorf("invalid package name %q", name)
	}
	return nil
}

type renderContext struct {
	db                 *goschema.Database
	opts               Options
	enumsByName        map[string]goschema.Enum
	fieldsByTable      map[string][]goschema.Field
	indexesByTable     map[string][]goschema.Index
	constraintsByTable map[string][]goschema.Constraint
	rlsByTable         map[string][]goschema.RLSPolicy
	rlsEnabledByTable  map[string][]goschema.RLSEnabledTable
	triggersByTable    map[string][]goschema.Trigger
	imports            map[string]struct{}
}

func newRenderContext(db *goschema.Database, opts Options) *renderContext {
	ctx := &renderContext{
		db:                 db,
		opts:               opts,
		enumsByName:        make(map[string]goschema.Enum, len(db.Enums)),
		fieldsByTable:      make(map[string][]goschema.Field),
		indexesByTable:     make(map[string][]goschema.Index),
		constraintsByTable: make(map[string][]goschema.Constraint),
		rlsByTable:         make(map[string][]goschema.RLSPolicy),
		rlsEnabledByTable:  make(map[string][]goschema.RLSEnabledTable),
		triggersByTable:    make(map[string][]goschema.Trigger),
	}
	for _, enum := range db.Enums {
		ctx.enumsByName[enum.Name] = enum
	}
	for _, field := range db.Fields {
		ctx.fieldsByTable[field.StructName] = append(ctx.fieldsByTable[field.StructName], field)
	}
	for _, index := range db.Indexes {
		ctx.indexesByTable[indexOwner(index)] = append(ctx.indexesByTable[indexOwner(index)], index)
	}
	for _, constraint := range db.Constraints {
		ctx.constraintsByTable[constraintOwner(constraint)] = append(ctx.constraintsByTable[constraintOwner(constraint)], constraint)
	}
	for _, policy := range db.RLSPolicies {
		ctx.rlsByTable[policy.StructName] = append(ctx.rlsByTable[policy.StructName], policy)
	}
	for _, table := range db.RLSEnabledTables {
		ctx.rlsEnabledByTable[table.StructName] = append(ctx.rlsEnabledByTable[table.StructName], table)
	}
	for _, trigger := range db.Triggers {
		ctx.triggersByTable[trigger.Table] = append(ctx.triggersByTable[trigger.Table], trigger)
	}
	return ctx
}

func (ctx *renderContext) renderSingleFile() (File, error) {
	source, err := ctx.renderFile(func(w *sourceWriter) {
		ctx.writeEnums(w)
		ctx.writeGlobalObjects(w)
		for _, table := range sortedTables(ctx.db.Tables) {
			ctx.writeTable(w, table)
		}
	})
	if err != nil {
		return File{}, err
	}
	return File{Name: "schema.go", Data: source}, nil
}

func (ctx *renderContext) renderPerTableFiles() ([]File, error) {
	files := make([]File, 0, len(ctx.db.Tables)+2)
	if len(ctx.db.Enums) > 0 {
		data, err := ctx.renderFile(func(w *sourceWriter) {
			ctx.writeEnums(w)
		})
		if err != nil {
			return nil, err
		}
		files = append(files, File{Name: "enums.go", Data: data})
	}
	if ctx.hasGlobalObjects() {
		data, err := ctx.renderFile(func(w *sourceWriter) {
			ctx.writeGlobalObjects(w)
		})
		if err != nil {
			return nil, err
		}
		files = append(files, File{Name: "schema_objects.go", Data: data})
	}
	for _, table := range sortedTables(ctx.db.Tables) {
		data, err := ctx.renderFile(func(w *sourceWriter) {
			ctx.writeTable(w, table)
		})
		if err != nil {
			return nil, err
		}
		files = append(files, File{Name: fileNameForTable(table), Data: data})
	}
	return files, nil
}

func (ctx *renderContext) renderFile(writeBody func(*sourceWriter)) ([]byte, error) {
	ctx.imports = make(map[string]struct{})
	body := &sourceWriter{}
	writeBody(body)

	w := &sourceWriter{}
	w.writeString(generatedHeader)
	w.writeLine("package " + ctx.opts.PackageName)
	w.writeLine("")
	if len(ctx.imports) > 0 {
		w.writeLine("import (")
		for _, path := range sortedImportPaths(ctx.imports) {
			w.writeLine("\t" + strconv.Quote(path))
		}
		w.writeLine(")")
		w.writeLine("")
	}
	w.writeString(body.buf.String())

	formatted, err := format.Source(w.buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf("format generated Go: %w\n%s", err, w.buf.String())
	}
	return formatted, nil
}

func (ctx *renderContext) hasGlobalObjects() bool {
	return len(ctx.db.Schemas) > 0 ||
		len(ctx.db.Extensions) > 0 ||
		len(ctx.db.Functions) > 0 ||
		len(ctx.db.Views) > 0 ||
		len(ctx.db.MaterializedViews) > 0 ||
		len(ctx.db.Roles) > 0 ||
		len(ctx.db.Grants) > 0
}

func (ctx *renderContext) writeEnums(w *sourceWriter) {
	for _, enum := range sortedEnums(ctx.db.Enums) {
		w.writeComment(annotation("migrator:schema:enum",
			attr{name: "name", value: enum.Name, set: true},
			attr{name: "values", value: strings.Join(enum.Values, ","), set: len(enum.Values) > 0},
		))
	}
	if len(ctx.db.Enums) > 0 {
		w.writeLine("type PtahEnums struct{}")
		w.writeLine("")
	}
	for _, enum := range sortedEnums(ctx.db.Enums) {
		typeName := exportedIdentifier(enum.Name)
		w.writeLine("type " + typeName + " string")
		w.writeLine("")
		w.writeLine("const (")
		for _, value := range enum.Values {
			constName := typeName + exportedIdentifier(value)
			w.writeLine("\t" + constName + " " + typeName + " = " + strconv.Quote(value))
		}
		w.writeLine(")")
		w.writeLine("")
	}
}

func (ctx *renderContext) writeGlobalObjects(w *sourceWriter) {
	for _, schema := range sortedSchemas(ctx.db.Schemas) {
		w.writeComment(annotation("migrator:schema:schema",
			attr{name: "name", value: schema.Name, set: true},
			attr{name: "comment", value: schema.Comment, set: schema.Comment != ""},
		))
	}
	for _, extension := range sortedExtensions(ctx.db.Extensions) {
		w.writeComment(annotation("migrator:schema:extension",
			attr{name: "name", value: extension.Name, set: true},
			attr{name: "if_not_exists", value: strconv.FormatBool(extension.IfNotExists), set: extension.IfNotExists},
			attr{name: "version", value: extension.Version, set: extension.Version != ""},
			attr{name: "comment", value: extension.Comment, set: extension.Comment != ""},
		))
	}
	for _, function := range sortedFunctions(ctx.db.Functions) {
		w.writeComment(functionAnnotation(function))
	}
	for _, view := range sortedViews(ctx.db.Views) {
		w.writeComment(annotation("migrator:schema:view",
			attr{name: "name", value: view.Name, set: true},
			attr{name: "body", value: view.Body, set: true},
			attr{name: "with_check", value: strconv.FormatBool(view.WithCheck), set: view.WithCheck},
			attr{name: "comment", value: view.Comment, set: view.Comment != ""},
		))
	}
	for _, view := range sortedMaterializedViews(ctx.db.MaterializedViews) {
		w.writeComment(annotation("migrator:schema:matview",
			attr{name: "name", value: view.Name, set: true},
			attr{name: "body", value: view.Body, set: true},
			attr{name: "refresh_strategy", value: view.RefreshStrategy, set: view.RefreshStrategy != ""},
			attr{name: "comment", value: view.Comment, set: view.Comment != ""},
		))
	}
	for _, role := range sortedRoles(ctx.db.Roles) {
		w.writeComment(roleAnnotation(role))
	}
	for _, grant := range sortedGrants(ctx.db.Grants) {
		w.writeComment(grantAnnotation(grant))
	}
	if ctx.hasGlobalObjects() {
		w.writeLine("type PtahSchemaObjects struct{}")
		w.writeLine("")
	}
}

func (ctx *renderContext) writeTable(w *sourceWriter, table goschema.Table) {
	for _, policy := range ctx.rlsByTable[table.StructName] {
		w.writeComment(rlsPolicyAnnotation(policy))
	}
	for _, enabled := range ctx.rlsEnabledByTable[table.StructName] {
		w.writeComment(annotation("migrator:schema:rls:enable",
			attr{name: "table", value: enabled.Table, set: true},
			attr{name: "comment", value: enabled.Comment, set: enabled.Comment != ""},
		))
	}
	for _, trigger := range ctx.triggersByTable[table.QualifiedName()] {
		w.writeComment(triggerAnnotation(trigger))
	}
	for _, constraint := range ctx.constraintsByTable[table.StructName] {
		w.writeComment(constraintAnnotation(constraint))
	}
	w.writeComment(tableAnnotation(table))
	typeName := exportedIdentifier(firstNonEmpty(table.StructName, table.Name))
	w.writeLine("type " + typeName + " struct {")
	usedNames := map[string]struct{}{}
	for _, field := range ctx.fieldsByTable[table.StructName] {
		fieldName := ctx.fieldIdentifier(field, usedNames)
		w.writeComment("\t" + annotation("migrator:schema:field", fieldAttrs(field)...))
		w.writeLine("\t" + fieldName + " " + ctx.goType(field) + structTags(field, ctx.opts))
	}
	for _, index := range ctx.indexesByTable[table.StructName] {
		w.writeComment("\t" + annotation("migrator:schema:index", indexAttrs(index)...))
		w.writeLine("\t_ struct{}")
	}
	w.writeLine("}")
	w.writeLine("")
}

func (ctx *renderContext) fieldIdentifier(field goschema.Field, used map[string]struct{}) string {
	name := firstNonEmpty(field.FieldName, field.Name)
	identifier := exportedIdentifier(name)
	if ctx.opts.LowercaseFields {
		identifier = unexportedIdentifier(name)
	}
	base := identifier
	for i := 2; ; i++ {
		if _, exists := used[identifier]; !exists {
			used[identifier] = struct{}{}
			return identifier
		}
		identifier = fmt.Sprintf("%s%d", base, i)
	}
}

func (ctx *renderContext) goType(field goschema.Field) string {
	base := sqlTypeToGoType(field.Type)
	if _, ok := ctx.enumsByName[field.Type]; ok {
		base = exportedIdentifier(field.Type)
	}
	if base == "time.Time" {
		ctx.imports["time"] = struct{}{}
	}
	if field.Nullable && base != "[]byte" {
		return "*" + base
	}
	return base
}

func tableAnnotation(table goschema.Table) string {
	return annotation("migrator:schema:table",
		attr{name: "name", value: table.Name, set: true},
		attr{name: "schema", value: table.Schema, set: table.Schema != ""},
		attr{name: "engine", value: table.Engine, set: table.Engine != ""},
		attr{name: "charset", value: table.Charset, set: table.Charset != ""},
		attr{name: "collate", value: table.Collate, set: table.Collate != ""},
		attr{name: "primary_key", value: strings.Join(table.PrimaryKey, ","), set: len(table.PrimaryKey) > 0},
		attr{name: "comment", value: table.Comment, set: table.Comment != ""},
	)
}

