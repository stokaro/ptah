func annotation(name string, attrs ...attr) string {
	var builder strings.Builder
	builder.WriteString("//")
	builder.WriteString(name)
	for _, attr := range attrs {
		if !attr.set {
			continue
		}
		builder.WriteByte(' ')
		builder.WriteString(attr.name)
		builder.WriteByte('=')
		builder.WriteString(strconv.Quote(attr.value))
	}
	return builder.String()
}

func structTags(field goschema.Field, opts Options) string {
	var tags []string
	if opts.AddDBTags {
		tags = append(tags, fmt.Sprintf("db:%q", field.Name))
	}
	if opts.AddJSONTags {
		tags = append(tags, fmt.Sprintf("json:%q", field.Name))
	}
	if len(tags) == 0 {
		return ""
	}
	return " `" + strings.Join(tags, " ") + "`"
}

func sqlTypeToGoType(sqlType string) string {
	normalized := strings.ToLower(strings.TrimSpace(sqlType))
	if before, _, ok := strings.Cut(normalized, "("); ok {
		normalized = strings.TrimSpace(before)
	}
	switch normalized {
	case "bool", "boolean":
		return "bool"
	case "smallint", "int2", "smallserial", "serial2":
		return "int16"
	case "int", "integer", "int4", "serial", "serial4", "mediumint":
		return "int"
	case "bigint", "int8", "bigserial", "serial8":
		return "int64"
	case "real", "float4":
		return "float32"
	case "double", "double precision", "float8", "float":
		return "float64"
	case "bytea", "blob", "binary", "varbinary":
		return "[]byte"
	case "date", "time", "timestamp", "timestamp without time zone", "timestamp with time zone", "timestamptz", "datetime":
		return "time.Time"
	default:
		return "string"
	}
}

func indexOwner(index goschema.Index) string {
	if index.StructName != "" {
		return index.StructName
	}
	return exportedIdentifier(index.TableName)
}

func constraintOwner(constraint goschema.Constraint) string {
	if constraint.StructName != "" {
		return constraint.StructName
	}
	return exportedIdentifier(constraint.Table)
}

func fileNameForTable(table goschema.Table) string {
	name := table.QualifiedName()
	if name == "" {
		name = table.StructName
	}
	return sanitizeFileName(name) + ".go"
}

func isSafeFileName(name string) bool {
	return name == filepath.Base(name) && name != "." && name != ".." && !filepath.IsAbs(name)
}

func sanitizeFileName(name string) string {
	name = strings.ToLower(strings.TrimSpace(name))
	name = strings.ReplaceAll(name, ".", "_")
	var builder strings.Builder
	for _, r := range name {
		if r >= 'a' && r <= 'z' || r >= '0' && r <= '9' || r == '_' {
			builder.WriteRune(r)
			continue
		}
		builder.WriteByte('_')
	}
	result := strings.Trim(builder.String(), "_")
	if result == "" {
		return "schema"
	}
	return result
}

func exportedIdentifier(name string) string {
	return upperFirst(identifierFromName(name))
}

func unexportedIdentifier(name string) string {
	return lowerFirst(identifierFromName(name))
}

func identifierFromName(name string) string {
	if hasUppercase(name) && token.IsIdentifier(name) && !token.Lookup(name).IsKeyword() {
		return name
	}
	parts := strings.FieldsFunc(name, func(r rune) bool {
		return !unicode.IsLetter(r) && !unicode.IsDigit(r)
	})
	if len(parts) == 0 {
		parts = []string{"value"}
	}
	var builder strings.Builder
	for _, part := range parts {
		if part == "" {
			continue
		}
		runes := []rune(strings.ToLower(part))
		builder.WriteRune(unicode.ToUpper(runes[0]))
		for _, r := range runes[1:] {
			builder.WriteRune(r)
		}
	}
	result := builder.String()
	if result == "" {
		result = "Value"
	}
	if unicode.IsDigit([]rune(result)[0]) {
		result = "N" + result
	}
	if token.Lookup(result).IsKeyword() {
		result += "Value"
	}
	return result
}

func lowerFirst(name string) string {
	if name == "" {
		return name
	}
	runes := []rune(name)
	runes[0] = unicode.ToLower(runes[0])
	return string(runes)
}

func upperFirst(name string) string {
	if name == "" {
		return name
	}
	runes := []rune(name)
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}

func hasUppercase(name string) bool {
	for _, r := range name {
		if unicode.IsUpper(r) {
			return true
		}
	}
	return false
}

func firstNonEmpty(values ...string) string {
	for _, value := range values {
		if value != "" {
			return value
		}
	}
	return ""
}

type sourceWriter struct {
	buf bytes.Buffer
}

func (w *sourceWriter) writeString(value string) {
	w.buf.WriteString(value)
}

func (w *sourceWriter) writeLine(value string) {
	w.buf.WriteString(value)
	w.buf.WriteByte('\n')
}

func (w *sourceWriter) writeComment(value string) {
	w.writeLine(value)
}

